import 'package:flutter/material.dart';
import 'package:google_fonts/google_fonts.dart';

class AppTheme {
  // Primary Colors
  static const Color primaryColor = Color(0xFF2E7D8A);
  static const Color primaryLightColor = Color(0xFF4A9EAD);
  static const Color primaryDarkColor = Color(0xFF1A5C67);
  
  // Secondary Colors
  static const Color secondaryColor = Color(0xFFFF6B35);
  static const Color secondaryLightColor = Color(0xFFFF8A65);
  static const Color secondaryDarkColor = Color(0xFFE55722);
  
  // Background Colors
  static const Color backgroundColor = Color(0xFFF8FAFB);
  static const Color surfaceColor = Color(0xFFFFFFFF);
  static const Color cardColor = Color(0xFFFFFFFF);
  
